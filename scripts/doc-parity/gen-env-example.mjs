#!/usr/bin/env node
/**
 * Regenerates .env.example deterministically from the environment variables read
 * by production code (extract-env) unioned with the dynamic provider keys, minus
 * the test-only and platform-provided allowlists. Secrets are emitted as
 * 1Password `op://` placeholders, never literal values.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { extractEnv } from './extract-env.mjs';
import { extractProviderEnvKeys } from './extract-provider-env-keys.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..', '..');
const allow = JSON.parse(readFileSync(join(here, 'allowlist.json'), 'utf8')).env;

// Fixed category order; vars within a category keep this curated order. Any
// code-read var not listed here lands in "Other" (sorted) so new vars still
// surface in the template.
const CATEGORIES = [
  ['Provider API keys & endpoints', [
    'ANTHROPIC_API_KEY', 'ANTHROPIC_API_BASE', 'ANTHROPIC_API_VERSION',
    'OPENAI_API_KEY', 'OPENAI_API_BASE', 'OPENAI_API_VERSION',
    'GROQ_API_KEY', 'GOOGLE_API_KEY', 'MISTRAL_API_KEY', 'AZURE_OPENAI_API_KEY',
    'GITHUB_TOKEN', 'OLLAMA_HOST', 'OLLAMA_URL', 'LOCAL_LLM_BASE_URL', 'WORKBENCH_BASE_URL',
  ]],
  ['LLM / memory (mem0) configuration', [
    'LLM_PROVIDER', 'LLM_API_KEY', 'LLM_API_BASE', 'LLM_MODEL',
    'LLM_EMBEDDING_PROVIDER', 'LLM_EMBEDDING_API_KEY', 'LLM_EMBEDDING_MODEL',
    'LLM_EMBEDDING_BASE_URL', 'LLM_EMBEDDING_DIMS',
    'MEMORY_CACHE_ENABLED', 'MEMORY_CACHE_TTL', 'MEMORY_CACHE_MAX_SIZE', 'MEM0_API_KEY',
  ]],
  ['Vector database', [
    'VECTOR_DB_PROVIDER', 'VECTOR_DB_URL', 'VECTOR_DB_API_KEY', 'VECTOR_DB_ENVIRONMENT', 'VECTOR_DB_INDEX',
  ]],
  ['Redis / Upstash', [
    'REDIS_URL', 'UPSTASH_REDIS_REST_URL', 'UPSTASH_REDIS_REST_TOKEN',
    'UPSTASH_REDIS_REST_URL_PREVIEW', 'UPSTASH_REDIS_REST_TOKEN_PREVIEW',
  ]],
  ['MCP transport', [
    'MCP_TRANSPORT', 'MCP_HTTP_PORT', 'MCP_HTTP_ENDPOINT', 'MCP_HTTP_STATELESS',
    'MCP_BEARER_TOKEN', 'DELEGATION_MCP_WRITE_TOKEN',
  ]],
  ['Integrations', [
    'LINEAR_API_KEY', 'SONARCLOUD_TOKEN', 'PROMPTINTEL_API_KEY', 'AXIOM_TOKEN', 'AXIOM_DATASET',
    'PILOT_STRIPE_SECRET_KEY', 'TELEGRAM_BOT_TOKEN', 'TELEGRAM_CHAT_ID', 'DCYFR_LABS_API_URL',
  ]],
  ['Telemetry & runtime', [
    'NODE_ENV', 'DCYFR_AGENTS_VERSION',
    'DCYFR_TELEMETRY_WORKSPACE_ID', 'DCYFR_TELEMETRY_NODE_ID', 'DCYFR_TELEMETRY_REPO', 'DCYFR_TELEMETRY_SCHEMA_VERSION',
    'ENABLE_METACOG_RUNTIME', 'DELEGATION_ENABLED', 'ENABLE_EXECUTION_MODES', 'DOCKER_HOST',
  ]],
];

// Example values where the code has a meaningful default / format.
const EXAMPLE_VALUES = {
  OPENAI_API_VERSION: 'v6',
  ANTHROPIC_API_VERSION: 'v074',
};

export function computeCodeReadSet() {
  const exclude = new Set([...allow.testOnly, ...allow.platformProvided]);
  // Dynamic provider keys are derived structurally from packages/ai/memory/config.ts.
  return new Set([...extractEnv(), ...extractProviderEnvKeys()].filter((v) => !exclude.has(v)));
}

export function buildEnvExample(codeRead = computeCodeReadSet()) {
  const secrets = new Set(allow.secrets);
  const remaining = new Set(codeRead);
  const line = (name) => {
    remaining.delete(name);
    if (secrets.has(name)) return `${name}="op://<YourVault>/<Item>/<field>"`;
    if (name in EXAMPLE_VALUES) return `${name}=${EXAMPLE_VALUES[name]}`;
    return `${name}=`;
  };

  const out = [
    '# DCYFR dcyfr-ai Environment -- Example',
    '#',
    '# GENERATED by scripts/doc-parity/gen-env-example.mjs from the env vars read by',
    '# packages/ai. Regenerate with: npm run docs:gen:env',
    '#',
    '# Copy this file to .env and fill in values. Secrets use 1Password references:',
    '#   format: op://<Vault>/<Item>/<Field>   (install: bash scripts/setup-1password.sh)',
    '',
  ];

  for (const [title, vars] of CATEGORIES) {
    const present = vars.filter((v) => codeRead.has(v));
    if (!present.length) continue;
    out.push(`# --- ${title} ---`);
    for (const v of present) out.push(line(v));
    out.push('');
  }

  const other = [...remaining].sort();
  if (other.length) {
    out.push('# --- Other ---');
    for (const v of other) out.push(line(v));
    out.push('');
  }

  return out.join('\n').replace(/\n+$/, '\n');
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  const content = buildEnvExample();
  writeFileSync(join(repoRoot, '.env.example'), content);
  console.log('gen-env-example: wrote .env.example');
}
