#!/usr/bin/env node
/**
 * Emits scripts/doc-parity/provider-env-keys.json -- a static, committed
 * manifest of the LLM-provider environment variables read via dynamic lookups
 * in packages/ai/memory/config.ts (see extract-provider-env-keys.mjs).
 *
 * The manifest is the reviewable artifact: adding a provider row to config.ts
 * produces a visible manifest diff in the PR, and check-env.mjs both consumes
 * these keys for .env.example parity AND verifies the committed manifest is
 * current (regenerate-and-compare). Run `npm run docs:gen:provider-env`.
 */
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { extractProviderEnvKeys } from './extract-provider-env-keys.mjs';

const here = dirname(fileURLToPath(import.meta.url));

export const MANIFEST_PATH = join(here, 'provider-env-keys.json');

export function buildManifest() {
  return {
    _comment:
      'GENERATED by scripts/doc-parity/gen-provider-env-keys.mjs from the providerEnvKeys + providerBaseEnvKeys tables in packages/ai/memory/config.ts. Do not edit by hand; run `npm run docs:gen:provider-env`. These vars are read via dynamic env[providerEnvKeys[...]] lookups invisible to the static env extractor, so check-env + gen-env-example consume this manifest.',
    _source: 'packages/ai/memory/config.ts',
    keys: extractProviderEnvKeys(),
  };
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  writeFileSync(MANIFEST_PATH, JSON.stringify(buildManifest(), null, 2) + '\n');
  console.log('gen-provider-env-keys: wrote scripts/doc-parity/provider-env-keys.json');
}
